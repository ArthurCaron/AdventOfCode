	static class Star1 {

		static int getResult() {
			int[] formattedInput = getFormattedInput();
			int count = 0;

			for (int i1 : formattedInput) {
				count += i1;
			}

			return count;
		}

	}

	static class Star2 {

		static int getResult() {
			int[] formattedInput = getFormattedInput();
			Set<Integer> frequencyCount = new HashSet<>();

			boolean searching = true;
			int repeatedFrequency = 0;

			int count = 0;
			frequencyCount.add(count);
			while (searching) {
				for (int i1 : formattedInput) {
					count += i1;
					if (frequencyCount.contains(count)) {
						searching = false;
						repeatedFrequency = count;
						break;
					} else {
						frequencyCount.add(count);
					}
				}
			}

			return repeatedFrequency;
		}

	}

